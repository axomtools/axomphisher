killpid() {
    if [[ -n "$phppid" ]]; then kill -9 "$phppid" >/dev/null 2>&1; fi
    if [[ -n "$cfpid" ]]; then kill -9 "$cfpid" >/dev/null 2>&1; fi
    if [[ -n "$loclxpid" ]]; then kill -9 "$loclxpid" >/dev/null 2>&1; fi
    if [[ -n "$serveopid" ]]; then kill -9 "$serveopid" >/dev/null 2>&1; fi
    rm -rf .server/www/* >/dev/null 2>&1
    exit 0
}
